_RULES = """## Что МОЖНО делать

- Описывать факты из данных: «выручка в Москве — 1.2 млн», «жалобы выросли с 12 до 17».
- Сравнивать величины: «Москва опережает Казань в 3 раза по выручке».
- Выявлять закономерности: «корреляция между временем ответа и оценкой отрицательная».
- Делать аналитические выводы: «концентрация проблем в трёх регионах из десяти».
- Описывать динамику: «жалобы растут на 8-12% в месяц».
- Структурировать проблемы: «42% жалоб — про скорость, 28% — про качество».

## Что СТРОГО ЗАПРЕЩЕНО

- НИКАКИХ рекомендаций, советов, предписаний, планов действий.
- НЕ использовать слова: «следует», «необходимо», «нужно», «рекомендуется», \
  «стоит внедрить», «предлагается», «требуется».
- НЕ создавать слайдов с заголовками «Рекомендации», «План действий», \
  «Что делать», «Следующие шаги», «Меры по устранению».
- Если в исходной суммаризации есть рекомендации — игнорируй их, бери только \
  фактологическую часть.

## Структура повествования

Контекст → метрики → распределения → динамика → закономерности → итоги анализа.
Без рекомендательной части. 8-13 слайдов, лучше плотнее и меньше.

## Графики

Тип выбирается осознанно:
- **bar / column** — сравнение категорий.
- **line** — динамика во времени.
- **pie** — структура целого. Только если категорий 3-6.
- **scatter** — корреляция двух величин (первая серия = X, вторая = Y).

Используй ТОЛЬКО реальные значения из данных. Заголовки графиков краткие (до 60 симв).

## Лимиты длин (ОБЯЗАТЕЛЬНЫ — превышение ломает вёрстку)

- Заголовок слайда: ≤ 80 символов
- Буллет: ≤ 130 символов
- Описание раздела: ≤ 200 символов
- KPI value: ≤ 10 символов («+24%», «1.2M», «8.4ч»)
- KPI label: ≤ 40 символов
- Инсайт в chart_with_text: ≤ 120 символов
- Заголовок графика: ≤ 60 символов
- Категория графика: ≤ 25 символов

## Доступные типы слайдов

`title`, `section_header`, `bullets`, `two_column`, `chart`,
`chart_with_text`, `kpi`, `quote`, `closing`.

`quote` — это вынесенный главный ФАКТ из данных, не цитата человека.
`closing` — нейтральный финал, БЕЗ рекомендаций.

## Палитра

6 цветов под тему и mood в hex без `#`:
- primary, secondary, accent (контрастный)
- background (светлый фон контента)
- text_dark (для светлого фона), text_light (для тёмного фона)

Не используй generic blue по умолчанию — подбирай под тему.
"""


