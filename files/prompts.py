def _full_instruction() -> str:
    """Собирает полную инструкцию: правила + few-shot пример."""
    return f"""Ты создаёшь профессиональную аналитическую презентацию для PowerPoint.
Твоя задача — превратить данные из Excel в валидный JSON по строгой схеме.

{_RULES}

## Пример (ВНИМАТЕЛЬНО ИЗУЧИ ФОРМАТ)

Пример входных данных:

```
{_EXAMPLE_INPUT_DATA}
```

Пример пожеланий по стилю: "{_EXAMPLE_USER_STYLE}"

Правильный ответ (точно такой формат должен быть и в твоём ответе):

```json
{_EXAMPLE_OUTPUT_JSON}
```

## Формат ответа (КРИТИЧНО)

Верни ТОЛЬКО валидный JSON по схеме, как в примере выше. Без markdown-обёрток
(никаких ```json```), без пояснений до или после, без приветствий и комментариев.
Первый символ ответа должен быть `{{`, последний — `}}`.
"""


# --------------------------------------------------------------------------- #
# Публичные функции (используются tool.py)
# --------------------------------------------------------------------------- #

def system_prompt() -> str:
    """Системная часть промпта — для режима, когда мы управляем системным промптом."""
    return _full_instruction()


def analysis_user_prompt(data_context: DataContext, user_style_prompt: str) -> str:
    """
    Полная инструкция + данные в виде ОДНОГО user-сообщения.

    Используется в режиме `inject_system_prompt=False`, когда системный
    промпт уже занят другой персоной (например, «Агата»). Этот вариант
    самодостаточен — модель получит полный контекст в одном сообщении.
    """
    return f"""{_full_instruction()}

## ЗАДАЧА

Теперь, используя точно такой же формат как в примере выше, создай аналитическую
презентацию по следующим данным:

# ДАННЫЕ ИЗ EXCEL

{data_context.to_prompt_text()}

# ПОЖЕЛАНИЯ ПОЛЬЗОВАТЕЛЯ ПО СТИЛЮ

{user_style_prompt}

# ВАЖНО

- Используй ТОЛЬКО реальные числа из данных выше.
- Минимум 3 графика разных типов.
- Только наблюдения, БЕЗ рекомендаций.
- Соблюдай лимиты длин.
- Верни ТОЛЬКО JSON, без markdown-обёрток и текста вокруг.
"""


def data_only_user_prompt(data_context: DataContext, user_style_prompt: str) -> str:
    """
    Короткая user-часть для режима с системным промптом.

    Используется когда `inject_system_prompt=True` — основная инструкция
    уже в SystemMessage, а здесь только данные и задача.
    """
    return f"""# ДАННЫЕ ИЗ EXCEL

{data_context.to_prompt_text()}

# ПОЖЕЛАНИЯ ПО СТИЛЮ

{user_style_prompt}

Создай аналитическую презентацию по схеме из примера. Используй реальные числа,
строй минимум 3 графика, БЕЗ рекомендаций. Верни только JSON."""


# --------------------------------------------------------------------------- #
# Backward compatibility (старые имена)
# --------------------------------------------------------------------------- #

SYSTEM_PROMPT = system_prompt()


def build_user_prompt(data_context: DataContext, user_style_prompt: str) -> str:
    """Старое имя — оставлено для совместимости."""
    return data_only_user_prompt(data_context, user_style_prompt)


def get_json_schema_hint() -> str:
    """Старое имя — теперь возвращает few-shot пример."""
    return _EXAMPLE_OUTPUT_JSON
