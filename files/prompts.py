def _full_instruction() -> str:
    """Собирает полную инструкцию: правила + few-shot пример."""
    return f"""ВНИМАНИЕ: ты должен вернуть СТРОГО валидный JSON. Это критично.
Перечитай эти правила формата дважды:

1. Ответ — это ОДИН JSON-объект. Первый символ `{{`, последний `}}`.
2. НЕ оборачивай в ```json``` или любые другие маркеры.
3. НЕ пиши никакого текста до или после JSON (ни «Вот дайджест», ни «Готово»).
4. Используй ТОЛЬКО двойные кавычки `"`, никогда одинарные `'`.
5. Булевы значения только маленькими: `true` / `false` (НЕ True/False).
6. Пустое значение — `null` (НЕ None).
7. НЕ ставь запятую после последнего элемента массива или объекта.
8. Внутри строк-цитат можно использовать ёлочки « » — это нормально.
9. Все поля из примера ОБЯЗАТЕЛЬНЫ. Не пропускай ни одного.

{_RULES}

## Пример валидного дайджеста

Пример входных данных:

```
{_EXAMPLE_INPUT_DATA}
```

Пожелания по стилю: "{_EXAMPLE_USER_STYLE}"

Правильный ответ — ровно такой JSON (скопируй его СТРУКТУРУ точь-в-точь,
подставив свои данные):

{_EXAMPLE_OUTPUT_JSON}

## НАПОМИНАНИЕ ПЕРЕД ОТВЕТОМ

Верни ТОЛЬКО валидный JSON по схеме выше. Первый символ — `{{`, последний — `}}`.
Двойные кавычки. true/false/null маленькими. Без markdown. Без комментариев.
Без текста вокруг. Все поля обязательны.
"""


